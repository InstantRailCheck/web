-- ============================================================
-- Ban/restriction enforcement in the existing quota triggers
-- ============================================================

create or replace function check_route_report_quota()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  recent_count integer;
begin
  if new.user_id is null then
    return new;
  end if;

  if exists (
    select 1 from user_moderation_status
    where user_id = new.user_id
      and (status in ('restricted', 'permanently_banned')
           or (status = 'temporarily_banned' and ban_expires_at > now()))
  ) then
    raise exception 'Your account is currently restricted from submitting route reports.'
      using errcode = 'P0001';
  end if;

  new.created_at := now();

  perform pg_advisory_xact_lock(hashtext('route_reports_quota'), hashtext(new.user_id::text));

  select count(*) into recent_count
  from route_reports
  where user_id = new.user_id
    and created_at > now() - interval '10 minutes';

  if recent_count >= 20 then
    raise exception 'Too many route reports submitted recently. Please wait a few minutes and try again.'
      using errcode = 'P0001';
  end if;

  return new;
end;
$$;

