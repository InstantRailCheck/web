-- v7.2 "User-level moderation": progressive account enforcement
-- (restrict/temporarily suspend/permanently ban), private bank-addition
-- attribution, and an admin-initiated account-deletion workflow — see
-- the v7.2 plan for the full design/review history behind every choice
-- below. Bundles a new feature's table+function+trigger-extension changes
-- into one migration, same precedent as 20260713060000_add_admin_moderation.sql.

-- ============================================================
-- Schema
-- ============================================================

-- Row persists permanently once a user is ever acted on (including
-- through reactivation to 'active') — this is what gives the Auth-sync
-- state and transition_id somewhere to live across a reactivation, and
-- gives the profile page real status history. No row = never moderated =
-- active (see getUserModerationStatus/the enforcement triggers below).
create table user_moderation_status (
  user_id uuid primary key references auth.users(id) on delete cascade,
  status text not null check (status in ('active', 'restricted', 'temporarily_banned', 'permanently_banned')),
  reason_category text not null check (reason_category in ('spam', 'fabricated', 'duplicate', 'privacy', 'abuse', 'harassment', 'other')),
  note text not null check (length(note) between 1 and 500),
  ban_expires_at timestamptz,
  moderator_user_id uuid references auth.users(id) on delete set null,
  -- Regenerated by moderate_set_user_status on every transition. An
  -- Auth-sync attempt only ever writes its result if this still matches
  -- what it started with — see lib/authSync.ts's reconcileAuthSync.
  transition_id uuid not null default gen_random_uuid(),
  auth_sync_status text not null default 'pending' check (auth_sync_status in ('synced', 'pending')),
  auth_sync_error text check (auth_sync_error is null or length(auth_sync_error) <= 300),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint user_moderation_status_temp_ban_has_expiry check (status <> 'temporarily_banned' or ban_expires_at is not null),
  constraint user_moderation_status_non_temp_no_expiry check (status = 'temporarily_banned' or ban_expires_at is null)
);
alter table user_moderation_status enable row level security;
-- Zero policies — service-role only, same as moderation_actions/route_requests.

-- Private bank-addition attribution. banks.* is publicly SELECT-able (see
-- rlsManifest.mjs), so attribution can never live as a column on banks
-- itself without leaking it — RLS is row-level, not column-level.
create table bank_attributions (
  bank_id uuid primary key references banks(id) on delete cascade,
  added_by_user_id uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now()
);
alter table bank_attributions enable row level security;
-- Zero policies — service-role only.

-- target_id becomes nullable: content-deletion actions keep it (the
-- removed row's own id, which isn't a personal identifier and has no
-- erasure obligation); user-level actions (status changes, account
-- deletion, email reveal) carry ONLY subject_user_id, which is what
-- actually gets erased via ON DELETE SET NULL when the account is
-- deleted. Populating target_id with a user's UUID for these actions
-- would permanently retain it (target_id has no FK/erasure behavior),
-- defeating the erasure subject_user_id is meant to guarantee.
alter table moderation_actions alter column target_id drop not null;
alter table moderation_actions add column subject_user_id uuid references auth.users(id) on delete set null;

-- Dynamically discover and drop each existing single-column CHECK
-- constraint by its actual name rather than assuming Postgres's default
-- <table>_<column>_check naming — same "don't hardcode what pg_catalog
-- can tell you" precedent as the policy-drop loop in
-- 20260710233000_lock_down_report_table_reads.sql.
do $$
declare
  v_conname text;
begin
  select con.conname into v_conname
  from pg_constraint con
  join pg_attribute att on att.attnum = con.conkey[1] and att.attrelid = con.conrelid
  where con.conrelid = 'public.moderation_actions'::regclass
    and con.contype = 'c'
    and array_length(con.conkey, 1) = 1
    and att.attname = 'action_type';
  if v_conname is not null then
    execute format('alter table moderation_actions drop constraint %I', v_conname);
  end if;
end $$;
alter table moderation_actions add constraint moderation_actions_action_type_check
  check (action_type in ('delete', 'restrict', 'suspend', 'ban', 'reactivate', 'delete_account', 'reveal_email'));

do $$
declare
